<template>
  <section>
    <v-container align-content-center>
      <v-layout row wrap>
        <v-flex lg12 text-lg-center>
          <div v-if="!user.name && !loading">
            <v-btn @click="login('Google')" color="green">
              Google Login
            </v-btn>
            <v-btn @click="login('Github')" color="orange">
              Github Login
            </v-btn>
            <v-btn @click="login('Facebook')" color="blue">
              Facebook Login
            </v-btn>
            <v-btn @click="login('Twitter')" color="purple">
              Twitter Login
            </v-btn>
          </div>
          <div v-else-if="loading">
            <loader />
          </div>
          <div v-else>
            <v-btn @click="logout()" color="error">Logout</v-btn>
          </div>
        </v-flex>
      </v-layout>
    </v-container>
  </section>
</template>

<script>
import { mapState } from "vuex";
import loader from "./global/Loader";
export default {
  name: "Auth",
  components: {
    loader
  },
  methods: {
    login(provider) {
      this.$store.dispatch("Auth/login", provider);
    },
    logout() {
      this.$store.dispatch("Auth/logout");
    }
  },
  computed: {
    ...mapState(["loading"]),
    ...mapState("Auth", ["user"])
  }
};
</script>

